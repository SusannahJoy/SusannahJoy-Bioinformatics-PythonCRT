Num=[11,22,33,44,55,66,77,88,99,100]
#prints the complete list
print(Num[0:])
#prints the elements from the given position
print(Num[1:4+1])
#prints the difference according to given number
print(Num[::1])
print(Num[::2])
print(Num[1::2])
#prints the reverse order of the list
print(Num[::-1])